x = 1
y = 2

if x < y :
    print ("x가 y보다 작습니다.")  # 앞에 공백을 입력해야 합니다.
    print (x)                      # 공백 입력 주의!!
    print (y)                      # 공백 입력 주의!!
elif x == y :
    print ("x와 y가 같습니다.")
    print (x)
    print (y)
else : 
    print ("x가 y보다 큽니다.")
    print (x)
    print (y)

