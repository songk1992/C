x1 = 10
x2 = 20
x3 = 30
x4 = 40
x5 = 50

y1 = x1 + 30   # 더하기, 10 + 30
y2 = x2 - 30   # 빼기  , 20 - 30
y3 = x3 * 30   # 곱하기, 30 * 30
y4 = x4 / 30   # 나누기, 40 / 30
y5 = x5 % 30   # 몫    , 50 % 30

print (y1)     # 40
print (y2)     # -10
print (y3)     # 900
print (y4)     # 1.3333333333333333
print (y5)     # 20

a = 5          # 
b = 7          # 
c = a & b      # 비트합 연산자

print (bin(a)) # 0b101
print (bin(b)) # 0b111
print (c)      # 5
print (bin(c)) # 0b101

